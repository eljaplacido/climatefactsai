class AuditTrailEntry(BaseModel):
    """Single entry in an insight's audit trail."""
    step: int
    action: str
    source: str
    source_url: Optional[str] = None
    confidence: float
    timestamp: str
    details: Optional[str] = None


class InsightBenchmark(BaseModel):
    """Benchmark comparison for a specific metric."""
    metric: str
    claimed_value: Optional[str] = None
    benchmark_value: Optional[str] = None
    benchmark_source: str
    benchmark_reliability: int
    deviation_pct: Optional[float] = None
    assessment: str  # "CONSISTENT", "WITHIN_RANGE", "DEVIATION", "CONTRADICTION"
    explanation: str


class SourceEvaluationReport(BaseModel):
    """Detailed evaluation of a news source's reliability."""
    source_name: str
    overall_score: int
    editorial_standards: str
    fact_check_record: str
    transparency_level: str
    total_articles: int
    false_claim_rate: float
    benchmarks_used: List[str]
    strengths: List[str]
    weaknesses: List[str]
    recommendation: str
    confidence_explanation: str


